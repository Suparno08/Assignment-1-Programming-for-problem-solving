#include <stdio.h>
int main(){
	int i=10,j=i,a=2,b=3,c=4,d=5;
	// %d is called format specifier
	// %d is for int, %f is for float, %c is for char;
	printf("\nthe value of i is %d and value of j is %d",i,j);
	printf("\nthe value of a is %d and value of b is %d",a,b);
	printf("\nthe value of c is %d and value of d is %d",c,d);
	return 0;
}
